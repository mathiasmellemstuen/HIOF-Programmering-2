package com.company;

public class SIUnitToAstromicalUnit {

    public static double convertKgToMjup(double kg) {
        return kg / 1.898E27;
    }
    public static double convertKmToRjup(double km) {
        return km / 71492;
    }
    public static double convertKgToMsun(double kg) {
        return kg / 1.98892E30;
    }
    public static double convertKmToRsun(double km) {
        return km / 695700;
    }
}